#include <iostream>
using namespace std;
/*
반복문이란 
*/
int main() {
    for (int i = 0; i < 5; i++) {
        cout << i << "번째입니다" << endl;
    }
    return 0;
    /*
    1.반복문에 사용할 카운터(int i)의 값을 초기화
    2.반복문을 진행할 것 인지 '조건검사'(i < 5;)
    3.반복문 몸체를 수행 * 몸체란 :()뒤에 {}
    4.카운터의 값을 변화
    */
}



